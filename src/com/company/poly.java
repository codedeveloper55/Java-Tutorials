// Polymorphism in Java

/*
  What is Polymorphism?
  It is the ability of an object to take on many forms.
  It's most common use in OOP occurs when a parent class
  reference is used to refer a child class object.

  Any Java object that can pass more than one IS-A test is
  considered to be polymorphic.

  ~It is important to know that the only possible way to access an object is
   through a reference variable. A reference variable can only be of one type.
   Once declared, the type of a reference variable cannot be changed.

  ~The reference variable can be reassigned to other objects provided that it
   is not declared final. The type of the reference variable would determine
   the methods that it can invoke on the object.

  ~A reference variable can refer to any object of its declared type or any subtype
  of its declared type. A reference variable can be declared as a class or interface type.

 */


